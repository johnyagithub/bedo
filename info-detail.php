            <br>
            สัย : พืชล้มลุกจำพวกหญ้า ปลูกง่าย อายุสั้น สามารถเจริญเติบโตได้ดีในทุกภาคของประเทศไทย<br>
            <br>
            ราก : ระบบรากแบบรากฝอย มีราก 2 แบบ รากส่วนแรกมีการเจริญเติบโตในระยะเวลาสั้น ๆ ขณะข้าวโพดเป็นต้นกล้า
            และจะตายไปเมื่อต้นข้าวโพดโตขึ้น รากส่วนที่สองคือ รากที่เจริญมาจากลำต้น ซึ่งเกิดจากข้อส่วนล่างของลำต้นข้อแรก
            จะเจริญเติบโตอยู่ตลอดชีวิตของข้าวโพด<br>
            <br>
            ลำต้น : มีลักษณะตั้งตรง และค่อนข้างกลม เป็นลำต้นที่ไม่มีเนื้อไม้ เพราะจัดเป็นพืชใบเลี้ยงเดี่ยวประกอบด้วยข้อ
            และปล้อง บริเวณข้อมีเนื้อเยื่อเจริญ จุดกำเนิดราก, ตา และรอยกาบใบ ปล้องที่อยู่เหนือตามักพบร่องตา
            โดยลำต้นมีความสูงตั้งแต่ 30 เซนติเมตรขึ้นไป ขนาดเส้นผ่าศูนย์กลางของลำต้นประมาณ 2.5-5.0 เซนติเมตร
            และลำต้นสดมักมีสีเขียว แต่บางพันธุ์มีสีม่วง<br>
            <br>
            ใบ : เป็นใบ ประกอบด้วย กาบใบ และแผ่นใบ กาบใบจะหุ้มลำต้น ส่วนแผ่นใบแผ่กางออก มีเส้นกลางใบ
            เส้นแขนงใบเป็นแบบขนานไปกับใบ ข้าวโพดที่ได้รับการปรับปรุงพันธุ์ให้ทนต่ออัตราการปลูกสูง มักมีลักษณะใบตั้ง
            แผ่นใบด้านบนมีขนเพื่อเพิ่มพื้นที่ในการรับแสง ส่วนแผ่นใบด้านล่างจะเรียบ และมีปากใบจำนวนมาก<br>
            <br>
            ดอก : ข้าวโพดเป็นพืชที่มีช่อดอกตัวผู้ และช่อดอกตัวเมีย อยู่บนต้นเดียวกันแต่แยกกันอยู่คนละตำแหน่ง<br>
            <br>
            ผลและเมล็ด : ผลของข้าวโพดเป็นแบบ Caryopsis ที่มีเยื้อหุ้มผล ติดอยู่กับเยื่อหุ้มเมล็ด มีลักษณะเป็นเยื่อบาง ๆ
            ใสไม่มีสี เยื่อหุ้มผลและเยื่อหุ้มเมล็ด<br>
            <br>
            ชนิดของข้าวโพด จำแนกตามคุณสมบัติของแป้งในเมล็ด : ประกอบด้วยแป้ง 2 ชนิด คือ แป้งแข็ง (Hard starch)
            และแป้งอ่อน
            (Soft starch)<br>
            <br>
            1. ข้าวโพดป่า (Pod corn) เป็นข้าวโพดที่ใช้ในการศึกษาแหล่งกำเนิดข้าวโพด
            (สถาบันวิจัยและพัฒนาแห่งมหาวิทยาลัยเกษตรศาสตร์, 2560)
            ซึ่งปลูกในบริเวณแถบอเมริกากลางและอเมริกาใต้
